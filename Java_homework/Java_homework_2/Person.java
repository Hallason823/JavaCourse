public class Person {

    private String gender;
    private int age;
    private double weight;

    public void setGender(String gender) {
        this.gender = gender;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }

    public String getGender(){
        return this.gender;
    }
    
    public int getAge() {
        return this.age;
    }

    public double getWeight() {
        return this.weight;
    }
}
